def main():

    word = input("Enter a string :")
    word_b = ""
    length = len(word)-1
    for w in range(length,-1,-1):
        palin = word[w]
        word_b = (word_b+palin)
    if word == word_b:
        print(word,"is a palindrome")
    else:
        print(word,"is not a palindrome")



main()
